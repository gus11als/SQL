SELECT AI.ANIMAL_ID,AI.ANIMAL_TYPE, AI.NAME
FROM ANIMAL_INS AI
JOIN ANIMAL_OUTS A0 ON AI.ANIMAL_ID = A0.ANIMAL_ID
WHERE (AI.SEX_UPON_INTAKE = 'Intact Male' OR AI.SEX_UPON_INTAKE = 'Intact Female')
AND (A0.SEX_UPON_OUTCOME = 'Neutered Male' OR A0.SEX_UPON_OUTCOME = 'Spayed Female')
ORDER BY ANIMAL_ID;